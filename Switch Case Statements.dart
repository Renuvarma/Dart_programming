//Switch Statement (Instead of writing many if-else conditions)

void main(){
  
  var grade='k';
  
  switch(grade){
      
    case 'A':
      print("Excellent");
      break;
    case 'B':
      print("good");
      break;
    case 'C':
      print("Try hard");
      break;
    default:
      print("Better luck next time");
      break;
      
  }
  
}
