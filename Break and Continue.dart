//Break And Continue Statements
  
//Break Statement  stop the loop immediately.
  
 void main(){
   for(var i =0;i<=20;i++){
     if(i ==15){
       break;
     }
    
     print(i);
   }
 } 
  


//Continue Statement  skip the current iteration and jump to the next iteration.

void main(){
   for(var i =0;i<=20;i++){
     if(i ==15){
       continue;
     }
    
     print(i);
   }
 } 
