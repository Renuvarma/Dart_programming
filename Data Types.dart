/*Data Types(in this dart language before assigning a value we have to mention 
its data type or else we can also put 'var' keyword)*/

void main(){
  //Integer(means without decimal values)
  int a=10;
  print(a);
  
  //Double(means with decimal values)
  double b=100.565669;
  print(b);
  
  //String(alphabets or characters)
  String name='renu';
  var lastname="varma";
  print(name);
  print(lastname);
  
  //boolean(true or false)
  bool alive=true;
  bool dead=false;
  print(alive);
  print(dead);
  
}
